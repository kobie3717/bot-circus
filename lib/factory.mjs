/**
 * Install npm dependencies in performer directory
 */
export async function installDeps(performerDir) {
  await execFileAsync('npm', ['install', '--prefer-offline'], {
    cwd: performerDir,
    timeout: 60000
  });
}

/**
 * Start bot with PM2
 */
export async function launchPM2(safeName, performerDir) {
  await execFileAsync('pm2', [
    'start', join(performerDir, 'bot.mjs'),
    '--name', `${safeName}-bot`,
    '--cwd', performerDir,
    '--interpreter', 'node'
  ], { timeout: 15000 });

  // Save PM2 state
  await execFileAsync('pm2', ['save'], { timeout: 10000 });
}

/**
 * Save bot record to managed-bots manifest
 */
export async function recordManagedBot(record) {
  const bots = await loadManagedBots();
  bots.push({
    ...record,
    created_at: new Date().toISOString()
  });
  await saveManagedBots(bots);
}

/**
 * Main factory function — orchestrates everything
 */
export async function spawnBot({ octoToken, name, type, rolePrompt, allowedUserId }) {
  console.log(`[factory] Spawning ${type} bot: ${name}`);

  // 1. Get token
  let token = await getManagedBotToken(octoToken, name);
  if (!token) {
    throw new Error(`Token required. Create a bot via @BotFather:\n1. Message @BotFather\n2. Send /newbot\n3. Follow prompts\n4. Copy the token and reply here with: "token is [YOUR_TOKEN]"`);
  }

  // 2. Get bot info (username)
  const botInfo = await getBotInfo(token);

  // 3. Generate performer files
  const { safeName, performerDir } = await generatePerformer(name, token, type, rolePrompt, allowedUserId);

  // 4. Install deps
  await installDeps(performerDir);

  // 5. Launch with PM2
  await launchPM2(safeName, performerDir);

  // 6. Record in manifest
  await recordManagedBot({
    name,
    safeName,
    username: botInfo.username,
    telegram_id: botInfo.id,
    type,
    rolePrompt,
    performerDir
  });

  return {
    name,
    username: `@${botInfo.username}`,
    type,
    pm2Name: `${safeName}-bot`,
    performerDir
  };
}

/**
 * Stop and deregister a managed bot
 */
export async function killBot(safeName) {
  // PM2 stop
  try {
    await execFileAsync('pm2', ['stop', `${safeName}-bot`], { timeout: 10000 });
    await execFileAsync('pm2', ['delete', `${safeName}-bot`], { timeout: 10000 });
  } catch (_) {}

  // Remove from manifest
  const bots = await loadManagedBots();
  const updated = bots.filter(b => b.safeName !== safeName);
  await saveManagedBots(updated);
}
